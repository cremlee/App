public class NutritionListItem {
    public String Fat;
    public String Sugar;
    public String Sodium;
    public String Protein;
    public String Kilocal;
    public String Carbohydrate;

    public NutritionListItem(String fat, String sugar, String sodium, String protein, String kilocal, String carbohydrate) {
        Fat = fat;
        Sugar = sugar;
        Sodium = sodium;
        Protein = protein;
        Kilocal = kilocal;
        Carbohydrate = carbohydrate;
    }
}
